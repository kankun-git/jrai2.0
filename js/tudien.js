let jr = true;

const langchange = () => {
  let lang = document.getElementById('language');
  if(!jr)
  {
    lang.innerHTML = `<h4 class="headerFont">Jr</h4>`;
    jr = true;
  }
  else 
  {
    lang.innerHTML = `<h4 class="headerFont">Vie</h4>`;
    jr = false;
  }
}

const fill = searchtext => {
    let matches = dict.filter(word => {
        const searchvalue = new RegExp(`^${searchtext}`, 'gi');
        if(jr)
          return word.Jrai.match(searchvalue);
        else
          return word.Vietnamese.match(searchvalue);
    });

    if (searchtext.length === 0) {
        matches = [];
        result.textContent = '';
    }

    render(matches);
} 

const render = matches => {
    if(matches.length === 0) {
        return;
    }
    
    const jrelements = matches.map((m, i) => {
      const box = document.createElement('div');
      box.className = 'outputbox';
      const header = document.createElement('h4');
      header.append(m.Jrai);
      box.append(header);
      box.addEventListener('click', () => express(m));
      return box;
    });
    const vieelements = matches.map((m, i) => {
      const box = document.createElement('div');
      box.className = 'outputbox';
      const header = document.createElement('h4');
      header.append(m.Vietnamese);
      box.append(header);
      box.addEventListener('click', () => express(m));
      return box;
    });

    if(jr)
    {
      if(result.attributes.length != jrelements.length)
      {
        result.textContent = '';
        for(let i = 0; i < jrelements.length; i++)
          result.appendChild(jrelements[i]);

      }
    }
    else
    {
      if(result.attributes.length != vieelements.length)
      {
        result.textContent = '';
        for(let i = 0; i < vieelements.length; i++)
          result.appendChild(vieelements[i]);

      }
    }
}

const express = match => {
    result.innerHTML = '';
    p = match.Pronounce;
    let expression = `
        <div class="expressbox">
            <h4>${match.Jrai}: ${match.Vietnamese}<h4>
            <img src="../icon/speaker.png" alt="speaker" onclick="playAudio(p)">
            <h4>Ví dụ:</h4> 
            <p>${match.Example}</p>
            <p> => ${match.Meaning}</p>
        </div>
    `;

    expressed.innerHTML = expression;
}

const playAudio = url => {
  speaker.src = url;
  speaker.play();
}

search.addEventListener('input', () => fill(search.value));